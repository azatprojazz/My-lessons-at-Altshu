/* null - это отсутствие какого-либо значения. */

let emptyLet = null;
const emptyConst = null;

console.log(emptyLet + emptyConst); // 0
console.log(emptyLet - emptyConst); // 0
console.log(emptyLet * emptyConst); // 0
console.log(emptyLet / emptyConst); // NaN
console.log(emptyLet ** emptyConst); // 1
